// Code 128 (subset B) → SVG. مولّد pure-TS مضمّن، بلا أي حزمة أو خدمة خارجية.
// يُستخدم لطباعة باركود الأصل بجانب الـ QR. جدول الأنماط قياسي (Code128).
// ملاحظة: يُنصح بالتحقق من قابلية المسح مرة واحدة على طابعة/قارئ فعلي.
// ════════════════════════════════════════════════════════════════════════

// أنماط عرض الوحدات (0..106): 6 أرقام لكل نمط (bar,space,bar,space,bar,space)،
// عدا نمط الإيقاف (106) = 7 أرقام. المجموع لكل نمط = 11 وحدة (13 للإيقاف).
const PATTERNS: string[] = [
  "212222","222122","222221","121223","121322","131222","122213","122312","132212","221213","221312","231212",
  "112232","122132","122231","113222","123122","123221","223211","221132","221231","213212","223112","312131",
  "311222","321122","321221","312212","322112","322211","212123","212321","232121","111323","131123","131321",
  "112313","132113","132311","211313","231113","231311","112133","112331","132131","113123","113321","133121",
  "313121","211331","231131","213113","213311","213131","311123","311321","331121","312113","312311","332111",
  "314111","221411","431111","111224","111422","121124","121421","141122","141221","112214","112412","122114",
  "122411","142112","142211","241211","221114","413111","241112","134111","111242","121142","121241","114212",
  "124112","124211","411212","421112","421211","212141","214121","412121","111143","111341","131141","114113",
  "114311","411113","411311","113141","114131","311141","411131","211412","211214","211232","2331112",
];
const START_B = 104;
const STOP = 106;

/** يبني SVG لباركود Code128-B لنص ASCII قابل للطباعة (32..126). */
export function code128Svg(text: string, opts?: { moduleWidth?: number; height?: number; quietZone?: number }): string {
  const mw = opts?.moduleWidth ?? 2;      // عرض الوحدة (px)
  const h = opts?.height ?? 60;
  const quiet = opts?.quietZone ?? 10;    // هامش صامت (وحدات)
  const clean = (text || "").replace(/[^\x20-\x7E]/g, "").slice(0, 40) || " ";

  const codes: number[] = [START_B];
  for (let i = 0; i < clean.length; i++) codes.push(clean.charCodeAt(i) - 32);
  let sum = START_B;
  for (let i = 0; i < clean.length; i++) sum += (clean.charCodeAt(i) - 32) * (i + 1);
  codes.push(sum % 103);                   // checksum
  codes.push(STOP);

  // بناء المستطيلات: كل نمط سلسلة عروض تتناوب bar/space بدءًا بـ bar.
  let x = quiet;
  const rects: string[] = [];
  for (const code of codes) {
    const pat = PATTERNS[code] ?? PATTERNS[0];
    let isBar = true;
    for (const ch of pat) {
      const w = parseInt(ch, 10) * mw;
      if (isBar) rects.push(`<rect x="${x}" y="0" width="${w}" height="${h}" fill="#000"/>`);
      x += w; isBar = !isBar;
    }
  }
  const totalW = x + quiet;
  return `<svg xmlns="http://www.w3.org/2000/svg" width="${totalW}" height="${h}" viewBox="0 0 ${totalW} ${h}" shape-rendering="crispEdges">` +
    `<rect width="${totalW}" height="${h}" fill="#fff"/>${rects.join("")}</svg>`;
}
